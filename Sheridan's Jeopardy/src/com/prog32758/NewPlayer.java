package com.prog32758;

public class NewPlayer {
	private String name;
	private int Score;
	
	public NewPlayer() {
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return Score;
	}
	public void setScore(int score) {
		Score = score;
	}
}
